/**
 * 犯罪信息统计对象
 * @export
 * @class CrimeInfoStatistics
 */
export class CrimeInfoStatistics {

    /**
     * 日期
     * @type {string}
     * @memberof CrimeInfoStatistics
     */
    date: string;

    /**
     * 个数
     * @type {number}
     * @memberof CrimeInfoStatistics
     */
    count: number;

    /**
     * 中心
     * @type {string}
     * @memberof CrimeInfoStatistics
     */
    center: string;

    /**
     * 中心编码
     * @type {string}
     * @memberof CrimeInfoStatistics
     */
    centerCode: string;

    /**
     * 类型
     * @type {string}
     * @memberof CrimeInfoStatistics
     */
    criminalType: string;
}
