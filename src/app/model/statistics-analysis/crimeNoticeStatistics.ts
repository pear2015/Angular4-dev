/**
 * 犯罪信息统计对象
 * @export
 * @class CrimeNoticeStatistics
 */
export class CrimeNoticeStatistics {

    /**
     * 日期
     * @type {string}
     * @memberof CrimeNoticeStatistics
     */
    date: string;

    /**
     * 个数
     * @type {number}
     * @memberof CrimeNoticeStatistics
     */
    count: number;

    /**
     * 法院
     * @type {string}
     * @memberof CrimeNoticeStatistics
     */
    court: string;

    /**
     * 法院编码
     * @type {string}
     * @memberof CrimeNoticeStatistics
     */
    courtCode: string;

}
